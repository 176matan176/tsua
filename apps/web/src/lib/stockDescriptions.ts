/**
 * Get a Hebrew one-line description of the company.
 * @param ticker e.g. "AAPL", "TEVA.TA", "MSTR"
 * @returns short Hebrew sentence, or null if we don't have one
 */
export function getStockDescription(ticker: string): string | null {
  if (!ticker) return null;
  const key = ticker.toUpperCase().replace('$', '');
  return DESCRIPTIONS[key] ?? null;
}
